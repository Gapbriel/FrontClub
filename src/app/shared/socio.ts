/**
 * Socio entity, used for filtering as well.
 */
export class Socio {
  /**
   * @type {number} id Unique numeric identifier.
   */
  id: number;
  
  /**
   * @type {string} nombre The nombre of the Socio.
   */
  nombre: String;

  /**
   * @type {string} fecha The fecha of the Socio.
   */
  fecha: String;

    /**
   * @type {string} estado The estado of the Socio.
   */
  estado: String;

   /**
   * @type {string} email The email of the Socio.
   */
  email: String;

}